        const music = @json($music);

        const musicNames = music.map(music => music.name);
        const musicRevenues = music.map(music => music.revenue);

        const ctx = document.getElementById('myChart').getContext('2d');
        const myChart = new Chart(ctx, {
            type: 'bar',
            data: {
                labels: musicNames,
                datasets: [{
                    label: 'Revenue',
                    data: musicRevenues,
                    backgroundColor: [
                        'rgba(255, 99, 132, 0.2)',
                        'rgba(54, 162, 235, 0.2)',
                        'rgba(255, 206, 86, 0.2)',
